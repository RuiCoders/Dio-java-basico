public class SwitchCase {
    public static void main(String[] args) {
        String plano = "T";

        switch (plano) {
            case "T": {
                System.out.println("5GB Youtube");
            
            }
            case "M": {
                System.out.println("Whats e Instagem gratis");
                break;
            }
            case "B": {
                System.out.println("100 inutos de ligação");
            }
        }
    }
}
